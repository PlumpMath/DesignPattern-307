package com.worthed.designpattern.Singleton;

/**
 * Singleton helper class for lazily initialization.
 *
 * Created by zhenguo on 12/6/14.
 */
public abstract class Singleton<T> {

    private T mInstance;

    protected abstract T create();

    public final T get() {
        synchronized (this) {
            if (mInstance == null) {
                mInstance = create();
            }
            return mInstance;
        }
    }

}
